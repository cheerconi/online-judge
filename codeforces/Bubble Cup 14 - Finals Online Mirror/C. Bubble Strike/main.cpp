const double eps = 1e-6;
double solve(int a, int b) {
    int tot = (a+b) * (a+b-1) * (a+b-2) / 3;
    int ret = 0;
    if (a >= 3) ret += a * (a-1) * (a-2) / 3;
    if (a >= 2 && b >= 1) ret += a * (a-1) * b;
    if (a >= 1 && b >= 2) ret += b * (b-1) / 2 * a;
    return (double)ret / tot;
}





