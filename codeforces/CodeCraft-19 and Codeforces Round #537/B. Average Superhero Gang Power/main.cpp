const int MAXN = 1e5 + 10;
int nums[MAXN], k, n;
LL cur;
LD ret;

void solve(LL len, LL m) {
    if (len == 0) return;
    LL tmp = cur + min(len*k, m);
    ret = max(ret, (LD)tmp/len);
}




