const int MAXN = 1e5 + 10;
int a[MAXN], b[MAXN];

bool solve(int n) {
  if (a[0] != b[0] || a[n-1] != b[n-1]) return false;
  vector<int> aa(n-1, 0), bb(n-1, 0);
  for (int i = 1; i < n; i++) {
    aa[i-1] = a[i]-a[i-1];
    bb[i-1] = b[i]-b[i-1];
  }
  sort(aa.begin(), aa.end());
  sort(bb.begin(), bb.end());
  for (int i = 0; i < n-1; i++) {
    if (aa[i] != bb[i]) return false;
  }
  return true;

}



