  int n; cin >> n;
  for (int i = 1; i <= n; i++) cin >> nums[i];
  for (int i = 1; i <= n; i++) {
    int sum = 0;
    for (int j = i; j >= 1; j--) {
      sum += nums[j];
      if (dp[j-1][0] <= sum) {
        dp[i][0] = sum;
        dp[i][1] = dp[j-1][1] + 1;
        break;
      }
    }
  }
  cout << n - dp[n][1] << endl;






