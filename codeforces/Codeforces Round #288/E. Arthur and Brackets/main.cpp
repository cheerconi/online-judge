  int n; cin >> n;
  for (int i = 0; i < n; i++) {
    cin >> nums[i][0] >> nums[i][1];
  }
  vector<int> dp;
  for (int i = n-1; i >= 0; i--) {
    int cur = 1;
    while (!dp.empty() && cur < nums[i][0]) {
      cur += dp.back();
      dp.pop_back();
    }
    if (nums[i][0] > cur || nums[i][1] < cur) {
      cout << "IMPOSSIBLE\n";
      return 0;
    }
    nums[i][0] = cur;
    dp.push_back(cur+1);
  }
  string s(2*n, '?');
  int idx = 0;
  for (int i = 0; i < n; i++) {
    while (s[idx] != '?') idx++;
    s[idx] = '(';
    s[idx+nums[i][0]] = ')';
  }
  cout << s << '\n';







