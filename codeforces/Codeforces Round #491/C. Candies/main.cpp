bool solve(LL n, LL k) {
    LL tot = 0;
    LL tmp = n;
    while (n > 0 && tot*2 < tmp) {
        tot += min(k, n);
        n -= min(k, n);
        n -= n/10;
    }
    return 2*tot >= tmp;
}



