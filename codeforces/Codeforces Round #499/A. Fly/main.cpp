const int MAXN = 1234;
const LD eps = 1e-9;
int a[MAXN], b[MAXN];
int m, n;

bool solve(LD val) {
    val += m;
    for (int i = 0; i < n; i++) {
        val = val - val / a[i];
        val = val - val / b[i];
        if (val+eps < m) break;
    }
    return val+eps > m;
}


