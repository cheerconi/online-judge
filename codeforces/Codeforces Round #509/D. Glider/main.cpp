    LL n, h, a, b;
    cin >> n >> h;
    for (int i = 0; i < n; i++) {
        cin >> a >> b;
        segment[i] = {a, b};
    }
    sort(segment, segment+n);
    LL ret = h;
    LL profit = segment[0].second - segment[0].first;
    int i = 0, j = 0;
    while (i < n) {
        while (j+1 < n) {
            LL need = segment[j+1].first - segment[i].first - profit;
            if (need >= h) break;
            j++;
            profit += segment[j].second - segment[j].first;
        }
        ret = max(ret, h+profit);
        profit -= segment[i].second - segment[i].first;
        i++;
    }
    cout << ret << endl;






