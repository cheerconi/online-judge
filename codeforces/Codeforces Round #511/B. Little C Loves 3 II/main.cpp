LL n, m;

LL solve() {
    LL ret = 0;
    if (n == 1) {
        ret += m / 6 * 6;
        m = m % 6;
        ret += max(0LL, 2*m - 6);
        return ret;
    }
    if (n == 2) {
        if (m == 2) return 0;
        if (m == 3) return 4;
        if (m == 7) return 12;
        return 2*m;
    }
    ret = n * m;
    if (ret % 2 == 1) ret--;
    return ret;
}




