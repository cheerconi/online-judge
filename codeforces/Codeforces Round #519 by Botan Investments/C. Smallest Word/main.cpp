vector<int> solve() {
    string s; cin >> s;
    int n = s.size();
    vector<int> ret(n, 0);
    int last_a = n;
    for (int i = n-1; i >= 0; i--) {
        if (s[i] == 'a') {
            last_a = i;
            break;
        }
    }
    if (last_a == n || last_a == 0) return ret;
    int i = 0;
    while (i < last_a) {
        if (s[i] != s[i+1]) {
            ret[i] = 1;
        }
        i++;
    }
    ret[last_a] = 1;
    return ret;
}




