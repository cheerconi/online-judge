void solve(LL n) {
  LL m = n / 2;
  LL tot = (n+1) * n / 2 * m;
  LL x = tot / n;
  if (x*m + (x+1)*m != tot) {
    cout << "NO\n";
    return;
  }
  vector<int> nums(n+1, 0);
  for (int i = 1; i <= m; i++) {
    int y = 2 * i;
    int x = y - 1;

    if (i % 2 == 0) {
      nums[i] = x;
      nums[i+m] = y;
    } else {
      nums[i] = y;
      nums[i+m] = x;
    }
  }
  cout << "YES\n";
  for (int i = 1; i <= n; i++) {
    cout << nums[i];
    if (i == n) cout << "\n";
    else cout << ' ';
  }
}




