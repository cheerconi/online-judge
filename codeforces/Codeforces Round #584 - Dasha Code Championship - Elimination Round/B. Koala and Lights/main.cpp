int a[123], b[123];
string s;
int solve(int t) {
  int tot = 0;
  int n = s.size();
  for (int i = 0; i < n; i++) {
    if (s[i] == '1') tot++;
    if (t < b[i]) continue;
    if ((t-b[i]) % a[i] == 0) s[i] = '1' - s[i] + '0';
  }
  return tot;
}




