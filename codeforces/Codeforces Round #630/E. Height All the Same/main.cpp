const int mod = 998244353;

LL power(LL base, LL p) {
  base = base % mod;
  LL ret = 1;
  while (p) {
    if (p&1) ret = ret * base % mod;
    base = base * base % mod;
    p >>= 1;
  }
  return ret;
}


