bool solve(int x, int n, int m) {
  while (x > 0) {
    int a = x/2 + 10;
    int b = x - 10;
    if (a < x && n > 0) {
      x = a;
      n--;
    } else if (m > 0) {
      x = b;
      m--;
    } else break;
  }
  return x <= 0;
}





