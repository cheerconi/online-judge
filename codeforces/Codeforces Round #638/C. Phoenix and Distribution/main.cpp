string solve(int k, string& s) {
  sort(s.begin(), s.end());
  string ret;
  ret.push_back(s[k-1]);
  if (s[0] != s[k-1]) return ret;
  if (k < s.size() && s[k] == s.back()) {
    int tot = s.size() - k;
    ret += string(tot/k, s[k]);
    if (tot % k != 0) ret.push_back(s[k]);
    return ret;
  }
  ret += s.substr(k);
  return ret;

}





