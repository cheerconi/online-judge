bool solve(int a, int b, int x) {
  if (x%2 == 0) {
    if (a == 0) return false;
    a--;
    x--;
  }
  if (b == 0) return false;
  b--;
  x--;
  x -= a;
  x -= b/2*2;
  if (x > 0) return false;
  return true;
}





