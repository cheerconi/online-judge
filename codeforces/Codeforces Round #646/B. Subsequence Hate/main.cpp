  while(t--) {
    string s; cin >> s;
    int n = s.size();
    memset(nums, 0, sizeof(nums));
    for (int i = 1; i <= n; i++) {
      nums[i] = nums[i-1];
      if (s[i-1] == '1') nums[i]++;
    }
    int ret = n, cur = 0;
    for (int i = n+1; i >= 1; i--) {
      if (i-1 < n && s[i-1] == '1') cur++;
      int tmp = nums[i-1] + n-i+1 - cur;
      ret = min(tmp, ret);
      tmp = i-1 - nums[i-1] + cur;
      ret = min(ret, tmp);
    }
    cout << ret << '\n';

  }






