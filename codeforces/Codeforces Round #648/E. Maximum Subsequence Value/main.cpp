const int MAXN = 512;
LL nums[MAXN];

LL solve(int n) {
  if (n == 1) return nums[0];
  if (n == 2) return nums[0] | nums[1];
  LL ret = 0;
  for (int i = 0; i < n; i++) {
    for (int j = i+1; j < n; j++) {
      LL cur = nums[i] | nums[j];
      for (int k = j+1; k < n; k++) {
        ret = max(ret, cur|nums[k]);
      }
    }
  }
  return ret;
}




