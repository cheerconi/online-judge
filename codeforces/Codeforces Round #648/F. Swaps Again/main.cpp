const int MAXN = 512;
int a[MAXN];

map<pii, int> solve(int n) {
  for (int i = 0; i < n; i++) {
    cin >> a[i];
  }
  map<pii, int> table;
  for (int i = 0; i < n/2; i++) {
    int mi = min(a[i], a[n-i-1]);
    int ma = max(a[i], a[n-i-1]);
    table[make_pair(mi, ma)]++;
  }
  if (n % 2 == 1) table[make_pair(-1, a[n/2])]++;
  return table;
}



