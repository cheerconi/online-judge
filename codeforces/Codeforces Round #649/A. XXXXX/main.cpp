  int t; cin >> t;
  while (t--) {
    int n, x; cin >> n >> x;
    int ret = -1;
    memset(rem, -1, sizeof(rem));
    set<int> table;
    table.insert(0);
    rem[0] = 0;
    for (int i = 1; i <= n; i++) {
      cin >> nums[i];
      nums[i] = (nums[i-1] + nums[i]) % x;
      if (rem[nums[i]] == -1) {
        rem[nums[i]] = i;
        table.insert(i);
      }
      auto it = table.begin();
      if (nums[*it] != nums[i]) {
        ret = max(ret, i - *it);
      }
      it++;
      if (it != table.end() && nums[*it] != nums[i]) {
        ret = max(ret, i - *it);
      }
    }
    cout << ret << '\n';
  }






