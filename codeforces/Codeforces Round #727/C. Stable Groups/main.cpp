  LL n, k, x; cin >> n >> k >> x;
  for (int i = 0; i < n; i++) {
    cin >> nums[i];
  }
  sort(nums, nums + n);
  vector<LL> splits;
  for (int i = 1; i < n; i++) {
    LL val = nums[i]-nums[i-1];
    if (val <= x) continue;
    splits.push_back(val/x);
    if (val % x == 0) splits.back()--;
  }
  sort(splits.begin(), splits.end());
  LL ret = splits.size() + 1;
  for (LL num : splits) {
    if (k < num) break;
    ret--;
    k -= num;
  }
  cout << ret << '\n';






