  int n; cin >> n;
  LL tot = 0;
  for (int i = 0; i < n; i++) {
    cin >> items[i].second >> items[i].first;
    tot += items[i].second;
  }
  sort(items, items+n);
  LL two = 0;
  LL one = 0;
  for (int i = 0; i < n; i++) {
    if (one + two == tot) break;
    LL a = items[i].first;
    LL b = items[i].second;
    if (one+two >= a) {
      LL tmp = min(tot - one - two, b);
      one += tmp;
    } else {
      LL tmp = min(tot-one-two, a-one-two);
      two += tmp;
      i--;
    }
  }
  assert(one+two == tot);
  cout << two * 2 + one << '\n';








