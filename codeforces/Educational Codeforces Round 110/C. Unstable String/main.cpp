  while (T--) {
    string s; cin >> s;
    int n = s.size();
    memset(dp, 0, sizeof(dp));
    LL ret = 0;
    for (int i = 0; i < n; i++) {
      if (s[i] != '1') {
        dp[0][i] = dp[1][i-1]+1;
      }
      if (s[i] != '0') {
        dp[1][i] = dp[0][i-1] + 1;
      }
      ret += max(dp[0][i], dp[1][i]);
    }
    cout << ret << '\n';
  }





