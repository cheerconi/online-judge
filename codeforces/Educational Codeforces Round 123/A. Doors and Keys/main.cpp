bool solve(const string& s) {
    set<int> table;
    for (char c : s) {
        int tmp = 0;
        if (c >= 'A' && c <= 'Z') {
            tmp = c - 'A';
            if (table.find(tmp) == table.end()) return false;
        } else {
            tmp = c - 'a';
            table.insert(tmp);
        }
    }
    return true;
}





