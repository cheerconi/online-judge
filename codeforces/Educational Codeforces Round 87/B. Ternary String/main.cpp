  while (t--) {
    string s; cin >> s;
    int cnt[3] = {0};
    int n = s.size();
    int ret = n+1;
    for (int i = 0, j = 0; i < n; i++) {
      while (j < n && (cnt[0] == 0 || cnt[1] == 0 || cnt[2] == 0)) {
        cnt[s[j] - '1']++;
        j++;
      }
      if (cnt[0] != 0 && cnt[1] != 0 && cnt[2] != 0) {
        ret = min(j-i, ret);
      }
      cnt[s[i]-'1']--;
    }
    if (ret == n+1) ret = 0;
    cout << ret << '\n';
  }






