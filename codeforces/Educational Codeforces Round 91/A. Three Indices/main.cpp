const int MAXN = 1234;
int nums[MAXN];
int a, b, c;

bool solve(int n) {
  for (int i = 0; i < n; i++) {
    int idx = i+1;
    for (int j = i+2; j < n; j++) {
      if (nums[i] < nums[idx] && nums[j] < nums[idx]) {
        a = i;
        b = idx;
        c = j;
        return true;
      }
      if (nums[j] > nums[idx]) idx = j;
    }
  }
  return false;
}




