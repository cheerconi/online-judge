LL gcd(LL a, LL b) {
  if (a > b) swap(a, b);
  if (a == 0) return b;
  return gcd(b%a, a);
}

LL solve(LL m, LL d, LL w) {
  LL x = min(m, d);
  if (x == 1) return 0;
  if (w == 1) return x * (x-1) / 2;
  w = w / gcd(d-1, w);
  LL n = x / w;
  return n * x  - (1 + n) * n / 2 * w;
}





