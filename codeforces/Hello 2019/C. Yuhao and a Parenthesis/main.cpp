    int n; cin >> n;
    string s;
    map<int, int>  a, b;
    int m = 0;
    for (int i = 0; i < n; i++) {
        int cur = 0, need = 0;
        cin >> s;
        for (char c : s) {
            if (c == '(') cur++;
            else cur--;
            if (cur < 0) {
                cur = 0;
                need++;
            }
        }
        if (need > 0) {
            if (cur != 0) continue;
            b[need]++;
        }
        else {
            if (cur == 0) m++;
            else a[cur]++;
        }
    }
    int ret = 0;
    for (auto it = a.begin(); it != a.end(); it++) {
        ret += min(it->second, b[it->first]);
    }
    ret += m/2;
    cout << ret << endl;




