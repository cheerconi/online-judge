const int MAXN = 1234;
int a[MAXN], b[MAXN];

bool solve(int n, int s) {
    if (a[1] == 0) return false;
    if (a[s] == 1) return true;
    if (b[s] == 0) return false;
    for (int i = n; i > s; i--) {
        if (a[i] == 1 && b[i] == 1) return true;
    }
    return false;
}




