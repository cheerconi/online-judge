LL gcd(LL a, LL b) {
    if (a < b) swap(a, b);
    if (b == 0) return a;
    return gcd(b, a % b);
}

LL solve(LL a, LL b, LL x, LL y) {
    if (b < x || y < a) return 0;
    return min(b, y) - max(a, x) + 1;
}





