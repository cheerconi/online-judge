void solve(int cs, string& s) {
    reverse(s.begin(), s.end());
    string t;
    for (char& c : s) {
        if (c == '4') {
            c = '2';
            t.push_back('2');
        } else {
            t.push_back('0');
        }
    }
    while (t.back() == '0') {
        t.pop_back();
    }
    reverse(s.begin(), s.end());
    reverse(t.begin(), t.end());
    cout << "Case #" << cs << ": " << s << " " << t << endl;
}




