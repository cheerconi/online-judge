const int MAXN = 1e5 + 10;
int nums[MAXN];

int solve(int n, int p) {
    int sum = 0;
    for (int i = 0; i < p; i++) {
        sum += nums[i];
    }
    int ret = nums[p-1] * p - sum;
    for (int i = p; i < n; i++) {
        sum += nums[i];
        sum -= nums[i-p];
        ret = min(ret, nums[i]*p - sum);
    }
    return ret;
}




