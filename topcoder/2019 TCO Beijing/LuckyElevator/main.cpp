int power(int p) {
    int ret = 1;
    while (p) {
        ret *= 9;
        p--;
    }
    return ret;
}

string s;
int solve(int i, bool bound) {
    if (i == s.size()) return 1;
    if (!bound) {
        return power(s.size()-i);
    }
    int ret = 0;
    for (int val = 0; val <= s[i]-'0'; val++) {
        if (val == 4) continue;
        ret += solve(i+1, val==s[i]-'0');
    }
    return ret;
}


class  LuckyElevator {
public:
    int actualFloor(int buttonPressed) {
        s = to_string(buttonPressed);
        return solve(0, true) - 1;
    }
};



