  freopen("numtri.out", "w", stdout);
  int n; cin >> n;
  vi dp(1);
  cin >> dp[0];
  int ret = dp[0];
  for (int i = 1; i < n; i++) {
    vi nxt;
    for (int j = 0; j < i+1; j++) {
      int val; cin >> val;
      nxt.push_back(val);
      int tmp = 0;
      if(j < i) tmp = max(tmp, dp[j]);
      if (j > 0) tmp = max(tmp, dp[j-1]);
      nxt.back() += tmp;
      ret = max(nxt.back(), ret);
    }
    dp = move(nxt);
  }
  cout << ret << endl;







